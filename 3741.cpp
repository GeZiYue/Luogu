template<class T1>void read(T1&);
template<class T1>void write(T1);
int main () {
    int n , i , ans = 0;
    string str;
    read(n);
    cin >> str;
    for (i = 0 ; i < n ; i++) {
        if (str[i] == 'V' && str[i + 1] == 'K') {
            ans++;
            str[i] = 'v';
            str[i + 1] = 'k';
        }
    }
    for (i = 0 ; i < n ; i++) {
        if (str[i] == str[i + 1]) {
            ans++;
            break;
        }
    }
    write(ans);
    EL;
    return 0;
}
template<class T1>void read(T1 &r_e_a_d) {
    T1 k = 0;
    char ch = getchar();
    int flag = 1;
    while(!isNum(ch) ) {
        if (ch == '-') {
            flag = -1;
        }
        ch = getchar();
    }
    while(isNum(ch) ) {
        k = (k << 1) + (k << 3) + ch - '0';
        ch = getchar();
    }
    r_e_a_d = flag * k;
}
template<class T1>void write(T1 w_r_i_t_e) {
    if (w_r_i_t_e < 0) {
        putchar('-');
        write(-w_r_i_t_e);
    }else {
        if (w_r_i_t_e < 10) {
            putchar(w_r_i_t_e + '0');
        }else {
            write(w_r_i_t_e / 10);
            putchar( (w_r_i_t_e % 10) + '0');
        }
    }
}
