template<class T>
inline void read(T &Re) {
  T k = 0;
  char ch = getchar();
  int flag = 1;
  while (!isNum(ch)) {
    if (ch == '-') {
      flag = -1;
    }
    ch = getchar();
  }
  while (isNum(ch)) {
    k = (k << 1) + (k << 3) + ch - '0';
    ch = getchar();
  }
  Re = flag * k;
}
template<class T>
inline void write(const T &Wr) {
  if (Wr < 0) {
    putchar('-');
    write(-Wr);
  } else {
    if (Wr < 10) {
      putchar(Wr + '0');
    } else {
      write(Wr / 10);
      putchar((Wr % 10) + '0');
    }
  }
}

void work(int n, ull k, bool rev) {
  if (n == 0) {
    return;
  }
  ull m = (1ull << (n - 1));
  if (k < m) {
    putchar(rev + '0');
    work(n - 1, k, false);
  } else {
    putchar((rev ^ 1) + '0');
    work(n - 1, k - m, true);
  }
}
